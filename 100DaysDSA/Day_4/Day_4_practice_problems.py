# 1. Reverse a String: Write a function that takes a string as input and returns the reversed string

# 2. Check if a String is a Palindrome: Write a function that takes a string as input and returns True if the string is a palindrome (reads the same forward and backward), ignoring case and non-alphanumeric characters

# 3. Count the Occurrences of a Character: Write a function that takes a string and a character as input and returns the count of occurrences of that character in the string

# 4. Remove Vowels from a String: Write a function that takes a string as input and returns a new string with all the vowels removed

# 5. Check if Two Strings are Anagrams: Write a function that takes two strings as input and returns True if the strings are anagrams (contain the same characters in a different order), ignoring case and whitespace

# 6. Find the Most Common Character in a String: Write a function that takes a string as input and returns the most common character in the string. If there are multiple characters with the same highest frequency, return any one of them

# 7. Reverse Words in a String: Write a function that takes a string as input and returns a new string with the order of words reversed. Assume that the words are separated by a single space